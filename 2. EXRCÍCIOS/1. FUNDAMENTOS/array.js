/* 
Em JavaScript, um array é uma estrutura de dados usada para armazenar uma coleção de valores em uma única variável. 
Os arrays são versáteis, dinâmicos e amplamente usados para manipular listas de dados. Cada elemento do array é associado a um índice, que começa em 0.

Os arrays podem conter diferentes tipos de dados.
*/


const valores = [7.7, 8.9, 9.2, 6.3];
console.log(valores);

//acessando determinado elemento
console.log(valores[3]);

//adicionando um índice e um valor
valores[4] = 10;

console.log(valores);
console.log(valores.length);
delete valores [0]
console.log(valores);

